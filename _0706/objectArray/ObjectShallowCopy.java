package _0706.objectArray;
// 얕은 복사로 array1의 값들을 비어있는 array2에 붙여넣기
public class ObjectShallowCopy {

	public static void main(String[] args) {

		Book[] bookArray1 = new Book[3];
		Book[] bookArray2 = new Book[3];
		
		bookArray1[0] = new Book("태백산맥", "조정래"); 
		bookArray1[1] = new Book("데미안", "헤르만헤세"); 
		bookArray1[2] = new Book("어떻게 살 것인가", "유시민"); 
		System.arraycopy(bookArray1, 0, bookArray2, 0, 3);
		
		for (int i = 0; i < bookArray2.length; i++) {
		bookArray2[i].showInfoBook(); // 기존 array1 요소들의 출력
		}
		
		bookArray1[0].setBookName("나목"); // array1 배열의 첫 번째 요소 값 변경
		bookArray1[0].setAuthor("박완서");
		
		System.out.println("==== bookArray1 ====");
		for (int i = 0; i < bookArray2.length; i++) {
			bookArray1[i].showInfoBook();
		}
		
		System.out.println("==== bookArray2 ====");
		for (int i = 0; i < bookArray2.length; i++) {
			bookArray2[i].showInfoBook(); // array1 값의 변경에 따라 array2도 변경되어 출력
		}
	}

}
